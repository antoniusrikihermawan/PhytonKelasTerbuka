# tipe data angka (satuan)
data_integer = 15
print('Data Integer = ',data_integer)
print('- data bertipe : ',type(data_integer))

# tipe data dengan koma (float)
data_float = 15.66
print('Data Float = ',data_float)
print('- data bertipe : ',type(data_float))

# tipe data kumpulan karakter (string)
data_string = 'Antonius Riki Hermawan 5455471'
print('Data string = ',data_string)
print('- data bertipe : ',type(data_string))

# tipe data biner False/True (boolean)
data_bool = True
print('Data Boolean = ',data_bool)
print('- data bertipe : ',type(data_bool))

# tipe data kompleks
data_complex = complex(6,4)
print('Data Complex = ',data_complex)
print('- data bertipe : ',type(data_complex))

# tipe data dari bahasa c
data_c_double = (10.5)
print('Data = ',data_c_double)
print('-bertipe : ',data_c_double)

